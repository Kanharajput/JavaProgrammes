/**
 * Here we talk about PreIncrement
 * @author Jaani
 */
public class PreIncrement {
    public static void main(String[] args) {
        int value1 = 10;
        int value2;
        value2 = ++value1;
        System.out.println("value2 is " + value2  + " and " + "value1 is " + value1);
    }
/** PreIncrement as the name show firstly Increment then assign 
 * here value1 get increment to 11 then assign to the value 2
 * Done 
 */
}
